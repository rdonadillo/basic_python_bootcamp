# Strings

# Enclosed in single quotes: ('...')
print('Hello World')
print('"Yes", he said')

# Enclosed in double quotes: ("...")
print("Hello World")

# Escape quotes: \
print('Doesn\'t')

# Raw strings: adding r before quote
print('First\nSecond')
print(r'First\nSecond')

# Can be indexed
word = 'Python'
print(word[0])